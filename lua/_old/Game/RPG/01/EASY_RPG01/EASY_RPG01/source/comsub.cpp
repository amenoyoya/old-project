//----------------------------------------------------------------------
//	中央に文字を出す。
//----------------------------------------------------------------------
void CenterDrawString( char *str, int fontsize, int y, int color )
{
	SetFontSize( fontsize );									//文字の大きさ
	int StrSizeX = GetDrawStringWidth(str,(int)strlen(str));	//文字表示の幅を得る。
	DrawString( (SCREEN_X-StrSizeX)/2, y, str, color );			//センターに表示
}















